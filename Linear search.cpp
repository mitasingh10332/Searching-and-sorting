#include<iostream>
#include<bits/stdc++.h>
using namespace std;

int main()
{
    //linear search
    int n,key;
    cin>>n>>key;
    int ar[n];

    for(int i=0;i<n;i++)
    {
        cin>>ar[i];
    }

    for(int i=0;i<n;i++)
    {
        if(ar[i]==key)
            {cout<<"key found at"<<i;break;}
    }
    return 0;
}
